<h2>operadores Relacuibaus </h2>
<hr>

<p>
    Operadores relacionais são utilizados
    para comparar a relação entre "coisas".
</p>


<ul>
    <li>>|Maior</li>
    <li><|Menor</li>
    <li>>=|Maior igual</li>
    <li><=|Menor igual</li>
    <li>!=|Diferente</li>
    <li>==|Igual</li>
</ul>

<P>
    Os operadores crelacionais retornam verdadeiro
    (true), ou falso (false), Em PHP true pode ser 
    representado por 1 ou por  uma variavel com valor
    e falso pode ser representado por 0 pela 
    inexisência de valor em uma variável.
</P>

