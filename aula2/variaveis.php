<h2>Variaveis</h2>>
<p>
    São estruturas (memória) Utilizada 
    para armazenar dados que serão processados pelo programa
</p>

<p>
  Para criar uma variavel em PHP basta colocar simbolo de
   $ na frente de um nome e atribuir um valor com
    o simbolo igual. Exemplo:
<pre>
     $variavel = 10.
     $nome = "Maria";
     $nota = 5.5;
</pre>
</p>

